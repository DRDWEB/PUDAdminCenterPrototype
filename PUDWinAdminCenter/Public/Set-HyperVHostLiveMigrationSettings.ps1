<#
    
    .SYNOPSIS
        Sets a computer's Hyper-V Host Live Migration settings.
    
    .DESCRIPTION
        Sets a computer's Hyper-V Host Live Migration settings.

    .NOTES
        This function is pulled directly from the real Microsoft Windows Admin Center

